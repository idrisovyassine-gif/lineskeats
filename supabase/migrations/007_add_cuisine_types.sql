-- Création de la table des types de cuisine
create table if not exists public.cuisine_types (
  id bigint generated by default as identity primary key,
  name text not null unique,
  icon text, -- emoji ou icône
  color text default '#emerald-500',
  created_at timestamptz not null default now()
);

-- Table de liaison entre restaurants et types de cuisine (many-to-many)
create table if not exists public.restaurant_cuisine_types (
  restaurant_id bigint references public.restaurants(id) on delete cascade,
  cuisine_type_id bigint references public.cuisine_types(id) on delete cascade,
  primary key (restaurant_id, cuisine_type_id)
);

