-- Activation du RLS
alter table public.cuisine_types enable row level security;
alter table public.restaurant_cuisine_types enable row level security;

-- Politiques pour les types de cuisine (lecture publique)
create policy "Cuisine types are public readable" on public.cuisine_types
for select using (true);

-- Politiques pour les liaisons restaurant-cuisine (gérées par le propriétaire)
create policy "Restaurant cuisine types are manageable by restaurant owner" on public.restaurant_cuisine_types
for all using (
  restaurant_id in (
    select id from public.restaurants where owner_id = auth.uid()
  )
)
with check (
  restaurant_id in (
    select id from public.restaurants where owner_id = auth.uid()
  )
);
